function M.get_all_words()
  local all = {}
  for _, cat in pairs(M.categories) do
    for _, word in ipairs(cat.words) do
      table.insert(all, word)
    end
  end
  return all
end

function M.get_category_words(category)
  local cat = M.categories[category]
  if cat then
    return cat.words
  end
  return {}
end

function M.get_category_list()
  local list = {}
  for key, cat in pairs(M.categories) do
    table.insert(list, { key = key, name = cat.name, count = #cat.words })
  end
  table.sort(list, function(a, b) return a.name < b.name end)
  return list
end

